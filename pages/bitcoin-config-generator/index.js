function detectOS () {
  if (typeof navigator === 'undefined') return null
  const ua = navigator.userAgent
  const platform = navigator.userAgentData?.platform ?? navigator.platform ?? ''
  if (/Win(dows|32|64|16)/i.test(ua) || /Windows/i.test(platform)) return 'windows'
  if (/Mac|iPhone|iPad/i.test(platform) || /Mac/i.test(ua)) return 'mac'
  if (/Linux|Android/i.test(ua) || /Linux/i.test(platform)) return 'linux'
  return null
}

/** Normalize value for comparison with default */
function norm (val, opt) {
  if (val === undefined || val === null || val === '') return ''
  if (opt.widget === 'switch') {
    if (val === true || val === 1 || val === '1') return '1'
    return '0'
  }
  if (opt.widget === 'multi_switch') {
    const s = String(val).trim()
    if (!s) return ''
    return s.split(',').map(x => x.trim()).filter(Boolean).sort().join(',')
  }
  if (opt.widget === 'number') {
    const n = Number(val)
    return isNaN(n) ? '' : String(n)
  }
  if (opt.widget === 'number_float_8') {
    const n = parseFloat(String(val))
    return isNaN(n) ? '' : String(n)
  }
  return String(val).trim()
}

/** True if current value differs from default (so we should include in conf) */
function differs (optionKey, value, opt) {
  const def = opt.default
  const v = norm(value, opt)
  const d = norm(opt.widget === 'switch' ? (def === '1' || def === 'true') : def, opt)
  if (v === d) return false
  if (v === '' && d === '') return false
  return true
}

/** Split multi-line/comma list and filter empty */
function splitMulti (text) {
  if (text == null || text === '') return []
  return String(text).split(/[\n,]/).map(s => s.trim()).filter(Boolean)
}

function buildConfig (opts, configPathForComment, effectiveOS = null) {
  const lines = []
  lines.push('# Generated by Stacker News Bitcoin config Generator')
  lines.push('# https://stacker.news/bitcoin-config-generator')
  lines.push('')
  lines.push('# This config should be placed in following path:')
  if (configPathForComment) {
    lines.push(`# ${configPathForComment}`)
  } else {
    lines.push('# Linux: ~/.bitcoin/bitcoin.conf')
    lines.push('# macOS: ~/Library/Application Support/Bitcoin/bitcoin.conf')
    lines.push('# Windows: %APPDATA%\\Bitcoin\\bitcoin.conf')
  }
  lines.push('')

  const push = (key, lineStr, comment) => {
    const opt = OPTIONS_BY_KEY[key]
    if (opt && comment !== false) lines.push(`# ${opt.label}`)
    lines.push(lineStr)
  }

  const emitOption = (key, opt, optWithDefault, val) => {
    if (opt.widget === 'multi_switch') {
      const items = (val && String(val).trim()) ? String(val).split(',').map(s => s.trim()).filter(Boolean) : []
      if (items.length > 0) {
        push(key, `${key}=${items[0]}`)
        for (let i = 1; i < items.length; i++) lines.push(`${key}=${items[i]}`)
      }
      return
    }
    if (opt.widget === 'switch') {
      const on = val === true || val === 1 || val === '1'
      push(key, `${key}=${on ? '1' : '0'}`)
      return
    }
    if (opt.widget === 'textarea') {
      let items = splitMulti(val)
      const defaultItems = splitMulti(opt.default)
      if (items.length === 0 && defaultItems.length === 0) return
      if (items.length === defaultItems.length && items.every((v, i) => v === defaultItems[i])) return
      // Bitcoin Core uses at most 8 addnode connections
      if (key === 'addnode' && items.length > 8) {
        lines.push('# Bitcoin Core uses at most 8 addnode connections.')
        items = items.slice(0, 8)
      }
      if (items.length > 0) {
        push(key, `${key}=${items[0]}`)
        for (let i = 1; i < items.length; i++) lines.push(`${key}=${items[i]}`)
      }
      return
    }
    if (opt.widget === 'number' || opt.widget === 'number_float_8') {
      const n = opt.widget === 'number' ? Number(val) : parseFloat(val)
      if (val !== '' && val != null && !isNaN(n)) {
        if (opt.widget === 'number_float_8' && n < 0) return
        // Bitcoin Core prune: only 0, 1, or ≥550 are valid; 2–549 are invalid (treat as manual)
        let emitVal = key === 'prune' && n > 1 && n < 550 ? 1 : val
        // Core enforces minimum 1 for timeout and peertimeout
        if ((key === 'timeout' || key === 'peertimeout') && n < 1) emitVal = 1
        // Core dbcrashratio is 0–1
        if (opt.widget === 'number_float_8' && opt.max != null && n > Number(opt.max)) emitVal = Number(opt.max)
        // Clamp to opt.min/opt.max so emitted config is valid (e.g. URL or preset out-of-range)
        let numEmit = opt.widget === 'number' ? Number(emitVal) : parseFloat(emitVal)
        if (!isNaN(numEmit)) {
          if (opt.min != null && numEmit < Number(opt.min)) numEmit = Number(opt.min)
          if (opt.max != null && numEmit > Number(opt.max)) numEmit = Number(opt.max)
          push(key, `${key}=${numEmit}`)
        }
      }
      return
    }
    if (val !== '' && val != null && String(val).trim() !== '') {
      push(key, `${key}=${val}`)
    }
  }

  let firstSection = true
  for (const cat of CATEGORIES) {
    const optionKeysWithChanges = []
    for (const key of cat.keys) {
      const opt = OPTIONS_BY_KEY[key]
      if (!opt) continue
      const pathDefault = OS_PATH_OPTION_KEYS.includes(key) && effectiveOS ? getDefaultPathForOption(key, effectiveOS) : null
      const effectiveDefault = pathDefault !== null ? pathDefault : opt.default
      const optWithDefault = effectiveDefault !== opt.default ? { ...opt, default: effectiveDefault } : opt
      // Treat any path-default value as matching current OS so we don't include them (avoids flash on load/OS change)
      // Treat empty select as option default so we don't include when "use default" is intended
      const rawVal = opts[key]
      const valueToCompare = pathDefault !== null && isPathDefaultValue(key, rawVal)
        ? pathDefault
        : (opt.widget === 'select' && (rawVal === '' || rawVal == null) && opt.default !== '' ? opt.default : rawVal)
      if (differs(key, valueToCompare, optWithDefault)) optionKeysWithChanges.push({ key, opt, optWithDefault })
    }
    if (optionKeysWithChanges.length === 0) continue

    if (!firstSection) lines.push('')
    firstSection = false
    lines.push(`# [${cat.title}]`)
    for (const { key, opt, optWithDefault } of optionKeysWithChanges) {
      // -test= only applies to regtest; omit and comment when chain is not regtest
      if (key === 'test' && opts[key] && String(opts[key]).trim() && opts.chain !== 'regtest') {
        lines.push('# -test= only applies to regtest; omitted for current chain.')
        continue
      }
      // acceptstalefeeestimates only applies to regtest; omit when chain is not regtest
      if (key === 'acceptstalefeeestimates' && (opts[key] === true || opts[key] === 1 || opts[key] === '1') && opts.chain !== 'regtest') {
        lines.push('# acceptstalefeeestimates only applies to regtest; omitted for current chain.')
        continue
      }
      emitOption(key, opt, optWithDefault, opts[key])
    }
  }

  if (!firstSection) {
    lines.push('')
    lines.push('# Network-specific sections')
    lines.push('# By default, the options above apply to every chain (main, test, testnet4, signet, regtest).')
    lines.push('# To restrict an option to one network, place it under the corresponding [section] below.')
    lines.push('# addnode, connect, port, bind, rpcport, rpcbind and wallet are mainnet-only unless')
    lines.push('# you repeat them under another section.')
    lines.push('')
    lines.push('# Mainnet')
    lines.push('[main]')
    lines.push('')
    lines.push('# Testnet3')
    lines.push('[test]')
    lines.push('')
    lines.push('# Testnet4')
    lines.push('[testnet4]')
    lines.push('')
    lines.push('# Signet')
    lines.push('[signet]')
    lines.push('')
    lines.push('# Regtest')
    lines.push('[regtest]')
    lines.push('')
  }

  return lines.join('\n')
}

function serializeOptsForUrl (opts, targetOS = null) {
  const o = {}
  Object.keys(opts).forEach(k => {
    const v = opts[k]
    if (v === DEFAULT_OPTIONS[k]) return
    if (k !== 'predefinedPreset' && (v === '' || v === null || v === undefined)) return
    o[k] = v
  })
  if (targetOS !== null && targetOS !== undefined && targetOS !== '') o.targetOS = targetOS
  return btoa(unescape(encodeURIComponent(JSON.stringify(o))))
}

function deserializeOptsFromUrl (hash) {
  if (!hash || !hash.startsWith('#')) return null
  try {
    const json = decodeURIComponent(escape(atob(hash.slice(1))))
    const o = JSON.parse(json)
    const targetOS = o.targetOS
    const { targetOS: _dropped, ...rest } = { ...getInitialState(), ...o }
    return { opts: rest, targetOS }
  } catch {
    return null
  }
}

